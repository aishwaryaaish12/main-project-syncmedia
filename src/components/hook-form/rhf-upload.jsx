RHFUpload.propTypes = {
  name: PropTypes.string,
  multiple: PropTypes.bool,
  helperText: PropTypes.node,
};

export function RHFUpload({ name, multiple, helperText, ...other }) {
  const { control } = useFormContext();

  return (
    <Controller
      name={name}
      control={control}
      render={({ field, fieldState: { error } }) => {
        console.log('Field Value:', field.value); // Log the field value here

        return multiple ? (
          <Upload
            multiple
            accept={{ 'image/*': ['.jpg', '.jpeg', '.png', '.gif', '.bmp', '.webp'], 'video/*':['.mp4','.mov','.avi'] ,'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet': ['.xlsx'],}}
            files={field.value}
            error={!!error}
            helperText={
              (!!error || helperText) && (
                <FormHelperText error={!!error} sx={{ px: 2 }}>
                  {error ? error?.message : helperText}
                </FormHelperText>
              )
            }
            {...other}
          />
        ) : (
          <Upload
            accept={{ 'image/*': ['.jpg', '.jpeg', '.png', '.gif', '.bmp', '.webp'], 'video/*':['.mp4','.mov','.avi'], 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet': ['.xlsx'],  }}
            file={field.value}
            error={!!error}
            helperText={
              (!!error || helperText) && (
                <FormHelperText error={!!error} sx={{ px: 2 }}>
                  {error ? error?.message : helperText}
                </FormHelperText>
              )
            }
            {...other}
          />
        );
      }}
/>
);
}